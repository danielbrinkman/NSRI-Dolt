    def preprocess(self, sql: str) -> str:
        """Remove Oracle-specific noise"""
        # Remove REM/PROMPT/SET lines
        sql = re.sub(r'^\s*rem.*$', '', sql, flags=re.MULTILINE | re.IGNORECASE)
        sql = re.sub(r'^\s*prompt.*$', '', sql, flags=re.MULTILINE | re.IGNORECASE)
        sql = re.sub(r'^\s*set.*$', '', sql, flags=re.MULTILINE | re.IGNORECASE)

        # Remove SQL*Plus variables like &tbs, &pass
        sql = re.sub(r'&\w+', '', sql)
        return sql

