    def convert_alter(self, stmt: str) -> str:
        # Convert VARCHAR2 
        stmt = re.sub(r'VARCHAR2', 'VARCHAR', stmt, flags=re.IGNORECASE)

        # Convert NUMBER
        stmt = re.sub(r'NUMBER\((\d+),(\d+)\)', r'DECIMAL(\1,\2)', stmt, flags=re.IGNORECASE)

        return stmt   
            
    def parse_columns(self, stmt: str):
        """Extract column definitions"""
        inside = re.search(r'\((.*)\)', stmt, re.DOTALL)
        if not inside:
            return []
    
        columns_block = inside.group(1)
    
        columns = []
        raw_columns = self.split_columns(columns_block)
        
        columns = []
        for col in raw_columns:
            parts = col.split()
            if not parts:
                continue
        
            columns.append({
                "name": parts[0],
                "raw": col
            })
        
        return columns
        
        
    def split_columns(self, columns_block: str):
        columns = []
        current = []
        depth = 0
        in_string = False
    
        for char in columns_block:
            if char == "'":
                in_string = not in_string
    
            elif not in_string:
                if char == '(':
                    depth += 1
                elif char == ')':
                    depth -= 1
    
            # Only split on commas at top level
            if char == ',' and depth == 0 and not in_string:
                columns.append(''.join(current).strip())
                current = []
            else:
                current.append(char)
    
        if current:
            columns.append(''.join(current).strip())
    
        return columns