    def get_statement_type(self, stmt: str):
        stmt_clean = stmt.strip().upper()
    
        if stmt_clean.startswith("CREATE TABLE"):
            return "CREATE_TABLE"
        elif stmt_clean.startswith("CREATE INDEX"):
            return "CREATE_INDEX"
        elif stmt_clean.startswith("ALTER TABLE"):
            return "ALTER_TABLE"
        elif stmt_clean.startswith("CREATE OR REPLACE VIEW"):
            return "VIEW"
        else:
            return "OTHER"

    def convert(self, sql: str) -> str:
        sql = self.preprocess(sql)
        statements = self.split_statements(sql)
    
        converted = []
        skipped = []
        
        for stmt in statements:
            stmt_type = self.get_statement_type(stmt)
            #print(stmt)
            if stmt_type == "CREATE_TABLE":
                converted.append(self.convert_create_table(stmt))
            elif stmt_type == "CREATE_INDEX":
                converted.append(self.convert_index(stmt))
            elif stmt_type == "ALTER_TABLE":
                converted.append(self.convert_alter(stmt))
            else:
                skipped.append(stmt_type)
                print("Skipped: ", stmt)
        print("Skipped:", set(skipped))

        return "\n\n".join(converted)
        
        
